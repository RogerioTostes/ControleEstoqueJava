    private void btBuscarPecaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btBuscarPecaActionPerformed
        // TODO add your handling code here:
        limparTabela();
        
        if(cbBuscarTodas.isSelected()){ // buscar todos os livros
            
            List<Cliente> lista = FormPricipal.daoCliente.todosClientes();
            for(Cliente p: lista){
                incluirCliente(p);
            }
        }
        else{ // buscar um so
            
            if(tfCpf.getText().trim().length() != 0){
                
               String cpf = tfCpf.getText();
                Cliente cliente = FormPricipal.daoCliente.buscarCliente(cpf);
                if(cliente != null){
                    incluirCliente(cliente);
                }
                else{
                    JOptionPane.showMessageDialog(null, "Cliente não encontrada", "Atenção", JOptionPane.ERROR_MESSAGE);
                }
            }
            else{
                JOptionPane.showMessageDialog(null, "Prencher o CPF", "Atenção", JOptionPane.ERROR_MESSAGE);
                
            }
        }
    }//GEN-LAST:event_btBuscarPecaActionPerformed

