    private void limparTabela(){
        for (int i = jTable1.getRowCount() - 1; i>=0; i--){
            modelo.removeRow(i);
        }
    }
    
    private void incluirCliente(Cliente cliente){
        modelo.addRow(new Object[]{cliente.getCpf(), cliente.getNome(), cliente.getEmail(), cliente.getTelefone(),
            cliente.getEstadoCivil()});
        
    }
    
    private Cliente getCliente(){
        Cliente cliente = new Cliente();
        int linha = jTable1.getSelectedRow();
        
        cliente.setCpf((String) modelo.getValueAt(linha, 0));
        cliente.setNome((String) modelo.getValueAt(linha, 1));
        cliente.setEmail((String) modelo.getValueAt(linha, 2));
        cliente.setTelefone((String) modelo.getValueAt(linha, 3));
        cliente.setEstadoCivil((String) modelo.getValueAt(linha, 4));
            
        return cliente;
    }
    
