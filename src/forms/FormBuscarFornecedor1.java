    private void btBuscarPecaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btBuscarPecaActionPerformed
        // TODO add your handling code here:
        limparTabela();
        
        if(cbBuscarTodas.isSelected()){ // buscar todos os livros
            
            List<Fornecedor> lista = FormPricipal.daoFornecedor.todosFornecedores();
            for(Fornecedor p: lista){
                incluirFornecedor(p);
            }
        }
        else{ // buscar um so
            
            if(tfCnpj.getText().trim().length() != 0){
                
               String cnpj = tfCnpj.getText();
                Fornecedor fornecedor = FormPricipal.daoFornecedor.buscarFornecedor(cnpj);
                if(fornecedor != null){
                    incluirFornecedor(fornecedor);
                }
                else{
                    JOptionPane.showMessageDialog(null, "Fornecedor não encontrada", "Atenção", JOptionPane.ERROR_MESSAGE);
                }
            }
            else{
                JOptionPane.showMessageDialog(null, "Prencher o CNPJ", "Atenção", JOptionPane.ERROR_MESSAGE);
                
            }
        }
    }//GEN-LAST:event_btBuscarPecaActionPerformed

