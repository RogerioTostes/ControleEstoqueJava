    private void limparTabela(){
        for (int i = jTable1.getRowCount() - 1; i>=0; i--){
            modelo.removeRow(i);
        }
    }
    
    private void incluirFornecedor(Fornecedor fornecedor){
        modelo.addRow(new Object[]{fornecedor.getCnpj(), fornecedor.getNome(), fornecedor.getEmail(), fornecedor.getTelefone(),
            fornecedor.getInsc()});
        
    }
    
    private Fornecedor getFornecedor(){
        Fornecedor fornecedor = new Fornecedor();
        int linha = jTable1.getSelectedRow();
        
        fornecedor.setCnpj((String) modelo.getValueAt(linha, 0));
        fornecedor.setNome((String) modelo.getValueAt(linha, 1));
        fornecedor.setEmail((String) modelo.getValueAt(linha, 2));
        fornecedor.setTelefone((String) modelo.getValueAt(linha, 3));
        fornecedor.setInsc((String) modelo.getValueAt(linha, 4));
            
        return fornecedor;
    }
    
